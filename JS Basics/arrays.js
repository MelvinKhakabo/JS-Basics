// Working with arrays in JS. --- arrays are like lists in python
//we use square brackets to define arrays[] and they can have differnt data types.
let selectedColors = ['red', 'blue'];
console.log(selectedColors);
//use indexing to find elements in an array
console.log(selectedColors[0]);
console.log(selectedColors[1]);
//add elements to an array -- add to the end of the array
selectedColors[2] = 'green';
console.log(selectedColors);
//add elements to an array -- add to the beginning of the array
selectedColors[0] = 'yellow';
console.log(selectedColors);
//length of an array
console.log(selectedColors.length);