// Declaring objects in JS --- objects are like dictionaries in python
//we use curly braces to define objects{} and they have key-value pairs.
//key-value pairs
let person = {
    name: 'John',
    age: 30
};
console.log(person);

//Changing values in objects
//dot notation
person.name = 'Mary';
console.log(person.name);
//bracket notation
person['name'] = 'Mike';
console.log(person.name);